package src.shapes;

import src.abilities.IAreaCalculatable;

public abstract class Shape implements IAreaCalculatable {
    private int posX, posY;

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }
}
